class Касса:
    def __init__(self, начальная_сумма=0):
        self.баланс = начальная_сумма

    def top_up(self, x):
        # Пополнить кассу
        if x < 0:
            raise ValueError("Нельзя пополнить отрицательной суммой.")
        self.баланс += x
        print(f"Касса пополнена на {x} руб. Текущий баланс: {self.баланс} руб.")

    def count_1000(self):
        # Возвращает количество целых тысяч в кассе
        return self.баланс // 1000

    def take_away(self, x):
        # Забрать сумму из кассы
        if x < 0:
            raise ValueError("Нельзя забирать отрицательную сумму.")
        if self.баланс < x:
            raise ValueError(f"В кассе недостаточно средств. Текущий баланс: {self.баланс} руб.")
        self.баланс -= x
        print(f"Из кассы забрано {x} руб. Остаток: {self.баланс} руб.")


# Основная программа с вводом данных от пользователя
try:
    начальный_баланс = int(input("Введите начальную сумму для кассы: "))
    касса = Касса(начальный_баланс)
    
    while True:
        print("\n--- Меню ---")
        print("1. Пополнить кассу")
        print("2. Забрать из кассы")
        print("3. Посмотреть количество целых тысяч")
        print("4. Выйти")
        выбор = input("Выберите действие (1-4): ")

        if выбор == '1':
            сумма = int(input("Введите сумму для пополнения: "))
            касса.top_up(сумма)
        elif выбор == '2':
            сумма = int(input("Введите сумму для снятия: "))
            касса.take_away(сумма)
        elif выбор == '3':
            print("Целых тысяч в кассе:", касса.count_1000())
        elif выбор == '4':
            print("Выход из программы.")
            break
        else:
            print("Неверный выбор. Попробуйте снова.")

except ValueError as e:
    print("Ошибка:", e)