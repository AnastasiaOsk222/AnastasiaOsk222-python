n = int(input("Введите количество чисел N: "))
numbers = list(map(int, input(f"Введите {n} целых чисел через пробел: ").split()))

# Проверка, что количество чисел совпадает с N
if len(numbers) != n:
    print("Ошибка: неверное количество чисел.")
else:
    # Используем множество для подсчёта уникальных элементов
    unique_numbers = set(numbers)
    
    # Выводим количество различных чисел
    print("Количество различных чисел:", len(unique_numbers))