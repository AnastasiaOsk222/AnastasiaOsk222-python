# Считываем первый список
print("Введите числа первого списка (в одной строке):")
list1 = list(map(int, input().split()))

# Считываем второй список
print("Введите числа второго списка (в одной строке):")
list2 = list(map(int, input().split()))

# Преобразуем списки в множества
set1 = set(list1)
set2 = set(list2)

# Находим пересечение множеств и его длину
common_count = len(set1 & set2)

# Выводим результат
print("Количество общих чисел:", common_count)